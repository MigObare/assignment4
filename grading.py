scores = int(input("Enter Marks: "))

if scores>=71 and scores<=100:
        print("Grade A") 
elif scores>=51 and scores<=70:
        print("Grade B")
elif scores >=31 and scores<=50:
        print("Grade C")
elif scores >=0 and scores<=30:
        print("Grade D")
else:
        print("Invalid Marks")
    

